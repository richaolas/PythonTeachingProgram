import turtle
turtle.penup()
turtle.goto(0,-200)
turtle.pendown()
turtle.pensize(20)
turtle.pencolor("#b0911c")
turtle.circle(200,360)
turtle.penup()
turtle.goto(-40,50)
turtle.setheading(90)
turtle.pendown()
turtle.circle(40,180)
turtle.penup()
turtle.goto(40,50)
turtle.setheading(90)
turtle.pendown()
turtle.circle(-40,180)
turtle.penup()
turtle.goto(-100,-60)
turtle.pendown()
turtle.circle(100,180)
turtle.penup()
turtle.goto(140,-30)
turtle.pencolor("#f797c6")
turtle.fillcolor("#f797c6")
turtle.begin_fill()
turtle.pendown()
turtle.circle(40,360)
turtle.end_fill()
turtle.penup()
turtle.goto(-140,-30)
turtle.pencolor("#f797c6")
turtle.fillcolor("#f797c6")
turtle.begin_fill()
turtle.pendown()
turtle.circle(-40,360)
turtle.end_fill()

turtle.done()