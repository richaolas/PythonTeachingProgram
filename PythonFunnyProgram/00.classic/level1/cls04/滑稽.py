import turtle as t
t.width(5)
t.speed(0)
#脸
# 三原色：红 黄 蓝  颜料
# 三基色：红 绿 蓝   光
t.colormode(1)
t.color(161/255,122/255,44/255)
# 写数字0-1
t.fillcolor(1,1,0)
t.begin_fill()
t.circle(150)
t.end_fill()
# 嘴
t.up()
t.goto(0, 130)
t.down()
t.dot(220,"black")
t.up()
t.goto(0, 140)
t.down()
t.dot(220,"yellow")
#左眼
t.fillcolor((1,1,1))
t.begin_fill()
t.up()
t.goto(-30,190)
t.down()
t.rt(30)
t.circle(15,180)
t.circle(150,50)
t.rt(5)
t.circle(15,180)
t.circle(-150,41)
t.end_fill()
t.up()
t.goto(-120,215)
t.dot(30,(88/255,36/255,10/255))
t.down()
#右眼
t.fillcolor((1,1,1))
t.begin_fill()
t.up()
t.goto(130,180)
t.down()
t.rt(10)
t.circle(15,180)
t.circle(150,50)
t.rt(5)
t.circle(15,180)
t.circle(-150,41)
t.end_fill()
t.up()
t.goto(45,215)
t.dot(30,(88/255,36/255,10/255))
t.down()
#脸颊
t.color('pink','pink')
t.up()
t.goto(120,140)
t.lt(40)
t.down()
t.begin_fill()
t.circle(10,150)
t.circle(50,40)
t.circle(10,150)
t.rt(10)
t.circle(50,40)
t.end_fill()
t.color('pink','pink')
t.up()
t.goto(-90,140)
t.rt(10)
t.down()
t.begin_fill()
t.circle(10,150)
t.circle(50,40)
t.circle(10,150)
t.rt(10)
t.circle(50,40)
t.end_fill()
t.done()