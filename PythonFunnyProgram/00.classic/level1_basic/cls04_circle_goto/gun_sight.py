import turtle
turtle.shape('turtle')
turtle.pensize(5)
turtle.penup()
turtle.goto(0, -200)
turtle.pendown()
turtle.circle(200)
turtle.left(90)
turtle.forward(400)
turtle.left(90)
turtle.circle(200, 90)
turtle.left(90)
turtle.forward(400)
turtle.goto(100, 0)
turtle.left(90)
turtle.forward(50)
turtle.backward(100)
turtle.penup()
turtle.goto(-100, 0)
turtle.pendown()
turtle.forward(50)
turtle.backward(100)
turtle.setheading(0)
turtle.penup()
turtle.goto(0, 100)
turtle.pendown()
turtle.forward(50)
turtle.backward(100)
turtle.penup()
turtle.goto(0, -100)
turtle.pendown()
turtle.forward(50)
turtle.backward(100)
turtle.penup()
turtle.goto(1000000, 1000000)
turtle.done()